//////////////////////////////////////////////////////////////////////////
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    print_num(7);
}
/////////////OUTPUT/////////////////////////////////////////////////////
//1 2 3 4 5 6 7
//////////////FUNCTION//////////////////////////////////////////////////
void print_num(int n)
{
    //base condition
    if(n==0)
    {
        return;
    }
    else
    {
        print_num(n-1);
        cout<<n<<" ";
    }
}
